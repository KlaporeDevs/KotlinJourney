fun add(a: Int, b: Int): Int {
    return a + b
}
fun sub(a: Int, b: Int): Int {
    return a - b
}
fun mul(a: Int, b: Int): Int {
    return a * b
}
fun div(a: Int, b: Int): Int {
    return a / b
}
fun mod(a: Int, b: Int): Int {
    return a % b
}
fun main(){
    println(add(10, 10))
    println(sub(10, 10))
    println(mul(10, 10))
    println(div(10, 10))
    println(mod(10, 100))
}